// relational or comparison operators
// > - more than
// < - less than
// >= - more than equal
// <= - less than equal

var result = 10 > 5
console.log(result)
result = 10 < 5
console.log(result)
result = 5 >= 5
console.log(result)
result = 5 <= 5
console.log(result)

// Equality operator
var x = 1
console.log(x == '1') // lose comparison
console.log(x === '1') // strict comparison